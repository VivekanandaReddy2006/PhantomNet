class TestTask5SIDUniqueness(unittest.TestCase):
    """Task 5: Verify SID uniqueness across all generated rules."""

    def setUp(self):
        """Reset SID counter for deterministic tests."""
        import sentinel.rule_generator as rg
        from sentinel.rule_generator import _SID_FILE_PATH

        self._original_exists = os.path.exists(_SID_FILE_PATH)
        self._original_val = None
        if self._original_exists:
            try:
                with open(_SID_FILE_PATH, "r") as f:
                    self._original_val = f.read()
            except Exception:
                pass

        if os.path.exists(_SID_FILE_PATH):
            try:
                os.remove(_SID_FILE_PATH)
            except Exception:
                pass

        if hasattr(rg, "_NEXT_SID"):
            with rg._sid_lock:
                rg._NEXT_SID = 3000001
        if hasattr(rg, "_current_sid"):
            with rg._sid_lock:
                rg._current_sid = 3000000

    def tearDown(self):
        import sentinel.rule_generator as rg
        from sentinel.rule_generator import _SID_FILE_PATH

        if self._original_exists and self._original_val is not None:
            try:
                os.makedirs(os.path.dirname(_SID_FILE_PATH), exist_ok=True)
                with open(_SID_FILE_PATH, "w") as f:
                    f.write(self._original_val)
                if hasattr(rg, "_NEXT_SID"):
                    with rg._sid_lock:
                        rg._NEXT_SID = rg._load_sid()
                if hasattr(rg, "_current_sid"):
                    with rg._sid_lock:
                        rg._current_sid = rg._load_sid() - 1
            except Exception:
                pass
        elif os.path.exists(_SID_FILE_PATH):
            try:
                os.remove(_SID_FILE_PATH)
            except Exception:
                pass

    def _extract_sid(self, rule_str):
        m = re.search(r"sid:(\d+);", rule_str)
        return int(m.group(1)) if m else None

    def test_all_scenario_rules_have_unique_sids(self):
        """All 8 scenario rules have unique SIDs."""
        sids = []
        for scenario in ATTACK_SCENARIOS:
            rule = generate_snort_rule(**scenario)
            sid = self._extract_sid(rule)
            self.assertIsNotNone(sid, "No SID found in rule")
            sids.append(sid)
        self.assertEqual(len(sids), len(set(sids)),
                         "Duplicate SIDs: %s" % sorted(sids))

    def test_50_sequential_sids_unique(self):
        """50 sequential auto-SID calls produce 50 unique SIDs."""
        sids = []
        for _ in range(50):
            rule = generate_snort_rule("any", 80, "tcp", "SID test", "T1234")
            sids.append(self._extract_sid(rule))
        self.assertEqual(len(sids), len(set(sids)))

    def test_sids_are_strictly_increasing(self):
        """Sequential SIDs are strictly increasing."""
        sids = []
        for _ in range(20):
            rule = generate_snort_rule("any", 80, "tcp", "SID test", "T1234")
            sids.append(self._extract_sid(rule))
        for a, b in zip(sids, sids[1:]):
            self.assertLess(a, b, "SIDs not increasing: %d >= %d" % (a, b))

    def test_campaign_rules_have_unique_sids(self):
        """All rules across all campaigns have unique SIDs."""
        all_sids = []
        for i, cluster in enumerate(CAMPAIGN_CLUSTERS):
            mitre = CAMPAIGN_MITRE_INFO[i % len(CAMPAIGN_MITRE_INFO)]
            result = generate_rules_for_campaign(cluster, mitre)
            for rule in result["snort_rules_list"]:
                sid = self._extract_sid(rule)
                self.assertIsNotNone(sid)
                all_sids.append(sid)
        self.assertGreater(len(all_sids), 0, "No SIDs collected")
        self.assertEqual(len(all_sids), len(set(all_sids)),
                         "Duplicate SIDs across campaigns: %s" % sorted(all_sids))

    def test_concurrent_sids_unique(self):
        """20 concurrent threads produce 20 unique SIDs."""
        results = []
        lock = threading.Lock()
        errors = []

        def worker():
            try:
                rule = generate_snort_rule("any", 80, "tcp", "concurrent", "T1234")
                sid = self._extract_sid(rule)
                with lock:
                    results.append(sid)
            except Exception as e:
                with lock:
                    errors.append(str(e))

        threads = [threading.Thread(target=worker) for _ in range(20)]
        for t in threads:
            t.start()
        for t in threads:
            t.join()

        self.assertFalse(errors, "Errors in threads: %s" % errors)
        self.assertEqual(len(results), len(set(results)),
                         "Duplicate concurrent SIDs: %s" % sorted(results))

    def test_all_sids_are_positive(self):
        """Every generated SID is a positive integer >= 1."""
        for scenario in ATTACK_SCENARIOS:
            rule = generate_snort_rule(**scenario)
            sid = self._extract_sid(rule)
            self.assertIsNotNone(sid)
            self.assertGreater(sid, 0, "SID must be positive, got %d" % sid)

    def test_explicit_sid_prevents_collision(self):
        """Explicit high SID advances counter to prevent future collisions."""
        high = 7_000_000
        generate_snort_rule("any", 80, "tcp", "explicit", "T1234", sid=high)
        next_rule = generate_snort_rule("any", 80, "tcp", "next auto", "T1234")
        next_sid = self._extract_sid(next_rule)
        self.assertEqual(next_sid, high + 1)


