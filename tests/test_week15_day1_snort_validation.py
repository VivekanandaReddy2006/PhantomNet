class TestTask2SemicolonPlacement(unittest.TestCase):
    """Task 2: Verify correct semicolon placement in rule options."""

    def _generate_test_rules(self):
        rules = []
        for scenario in ATTACK_SCENARIOS:
            rules.append(generate_snort_rule(**scenario))
        return rules

    def test_every_option_ends_with_semicolon(self):
        """Every option in the options block ends with a semicolon."""
        for rule in self._generate_test_rules():
            options_region = rule[rule.index("(") + 1: -1].strip()
            self.assertTrue(
                options_region.endswith(";"),
                "Options block doesn't end with ';': ...%s" % options_region[-30:],
            )

    def test_msg_option_semicolon_terminated(self):
        """msg option is properly semicolon-terminated (not inside quotes)."""
        for rule in self._generate_test_rules():
            # After msg:"..."; there must be a semicolon after the closing quote
            self.assertRegex(rule, r'msg:"[^"]*";\s')

    def test_sid_option_semicolon_terminated(self):
        """sid:NNNN; option is properly semicolon-terminated."""
        for rule in self._generate_test_rules():
            self.assertRegex(rule, r"sid:\d+;")

    def test_rev_option_semicolon_terminated(self):
        """rev:N; option is properly semicolon-terminated."""
        for rule in self._generate_test_rules():
            self.assertRegex(rule, r"rev:\d+;")

    def test_classtype_option_semicolon_terminated(self):
        """classtype:value; option is properly semicolon-terminated."""
        for rule in self._generate_test_rules():
            self.assertRegex(rule, r"classtype:[a-z\-]+;")

    def test_flow_option_semicolon_terminated(self):
        """flow:to_server,established; is properly semicolon-terminated."""
        for rule in self._generate_test_rules():
            self.assertIn("flow:to_server,established;", rule)

    def test_reference_option_semicolon_terminated(self):
        """reference:url,...; is properly semicolon-terminated."""
        for rule in self._generate_test_rules():
            self.assertRegex(rule, r"reference:url,[^;]+;")

    def test_threshold_option_semicolon_terminated(self):
        """threshold option is properly semicolon-terminated."""
        for rule in self._generate_test_rules():
            self.assertIn("seconds 60;", rule)

    def test_no_double_semicolons(self):
        """No unescaped double semicolons ;; in any rule."""
        for rule in self._generate_test_rules():
            # Strip escaped semicolons first, then check
            cleaned = rule.replace("\\;", "")
            self.assertNotIn(";;", cleaned,
                             "Double semicolons found in: %s" % rule[:80])

    def test_campaign_rules_semicolon_placement(self):
        """Campaign-generated rules have correct semicolon placement."""
        for i, cluster in enumerate(CAMPAIGN_CLUSTERS):
            mitre = CAMPAIGN_MITRE_INFO[i % len(CAMPAIGN_MITRE_INFO)]
            result = generate_rules_for_campaign(cluster, mitre)
            for rule in result["snort_rules_list"]:
                options_region = rule[rule.index("(") + 1: -1].strip()
                self.assertTrue(
                    options_region.endswith(";"),
                    "Campaign rule options not ';' terminated: %s" % rule[:60],
                )


