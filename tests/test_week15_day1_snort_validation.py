class TestTask4EscapeCharacters(unittest.TestCase):
    """Task 4: Verify proper escape characters in msg and content fields."""

    def test_semicolon_in_msg_is_escaped(self):
        """Semicolons inside msg are escaped as \\;"""
        rule = generate_snort_rule(
            "any", 80, "tcp", "End; payload; here", "T1190"
        )
        parsed = parse_snort_rule(rule)
        msg = parsed["options"]["msg"]
        self.assertIn("\\;", msg)

    def test_backslash_in_msg_is_doubled(self):
        """Backslashes inside msg are doubled \\\\."""
        rule = generate_snort_rule(
            "any", 80, "tcp", "Path\\to\\evil", "T1190"
        )
        parsed = parse_snort_rule(rule)
        msg = parsed["options"]["msg"]
        self.assertIn("\\\\", msg)

    def test_double_quote_in_msg_is_escaped(self):
        """Double-quotes inside msg are escaped as \\\"."""
        rule = generate_snort_rule(
            "any", 80, "tcp", 'Desc "quoted" value', "T1190"
        )
        parsed = parse_snort_rule(rule)
        msg = parsed["options"]["msg"]
        self.assertIn('\\"', msg)

    def test_combined_special_chars_in_msg(self):
        """Combined special characters are all properly escaped."""
        rule = generate_snort_rule(
            "any", 80, "tcp", 'Mixed; "quotes" and \\backslash', "T1190"
        )
        parsed = parse_snort_rule(rule)
        msg = parsed["options"]["msg"]
        self.assertIn("\\;", msg)
        self.assertIn('\\"', msg)
