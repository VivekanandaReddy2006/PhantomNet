class TestTask3ParenthesesUsage(unittest.TestCase):
    """Task 3: Verify correct parentheses usage in rule body."""

    def _generate_test_rules(self):
        rules = []
        for scenario in ATTACK_SCENARIOS:
            rules.append(generate_snort_rule(**scenario))
        return rules

    def test_rule_has_opening_parenthesis(self):
        """Every rule has an opening parenthesis."""
        for rule in self._generate_test_rules():
            self.assertIn("(", rule)

    def test_rule_ends_with_closing_parenthesis(self):
        """Every rule ends with a closing parenthesis."""
        for rule in self._generate_test_rules():
            self.assertTrue(rule.strip().endswith(")"))

    def test_balanced_parentheses(self):
        """Parentheses are balanced in every rule."""
        for rule in self._generate_test_rules():
            open_count = rule.count("(")
            close_count = rule.count(")")
            self.assertEqual(
                open_count, close_count,
                "Unbalanced parentheses (%d open, %d close) in: %s"
                % (open_count, close_count, rule[:80]),
            )

    def test_options_block_inside_parentheses(self):
        """Options block is entirely enclosed between ( and )."""
        for rule in self._generate_test_rules():
            paren_open = rule.index("(")
            self.assertTrue(rule.endswith(")"))
            options = rule[paren_open + 1: -1].strip()
            self.assertIn("msg:", options)
            self.assertIn("sid:", options)

    def test_header_before_parentheses(self):
        """Rule header (alert proto src ...) comes before the opening parenthesis."""
        for rule in self._generate_test_rules():
            paren_open = rule.index("(")
            header = rule[:paren_open].strip()
            self.assertTrue(header.startswith("alert"))
            tokens = header.split()
            self.assertEqual(len(tokens), 7, "Header has wrong token count: %s" % header)

    def test_campaign_rules_parentheses(self):
        """Campaign-generated rules have correct parentheses usage."""
        for i, cluster in enumerate(CAMPAIGN_CLUSTERS):
            mitre = CAMPAIGN_MITRE_INFO[i % len(CAMPAIGN_MITRE_INFO)]
            result = generate_rules_for_campaign(cluster, mitre)
            for rule in result["snort_rules_list"]:
                self.assertIn("(", rule)
                self.assertTrue(rule.strip().endswith(")"))
                self.assertEqual(rule.count("("), rule.count(")"))

    def test_parseable_by_snort_parser(self):
        """Every generated rule is parseable by the Snort rule parser."""
        for rule in self._generate_test_rules():
            try:
                parsed = parse_snort_rule(rule)
                self.assertIsNotNone(parsed)
            except ValueError as e:
                self.fail("Rule failed to parse: %s\nError: %s" % (rule[:80], e))


